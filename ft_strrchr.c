#include "libft.h"

char	*ft_strrchr(const char *str, int ch)
{
	char		*last;
	char		find;
	size_t		i;

	last = (char *)str;
	find = (char)ch;
	i = ft_strlen(str);
	while (i > 0)
	{
		if (last[i] == find)
			return (last + i);
		i--;
	}
	if (last[i] == find)
		return (last);
	return (0);
}
